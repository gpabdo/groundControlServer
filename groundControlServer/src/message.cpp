#include "message.h"

/******************************************************************
*
******************************************************************/
message::message(int theSource, int theCommand, int theValue, int theDest )
{
	source = theSource;
	dest = theDest;
	command = theCommand;
	value = theValue;
	data_length = 0;
}

/******************************************************************
*
******************************************************************/

message::message(int theSource, int theCommand, int theValue, int theDest, string value )
{
	source = theSource;
	dest = theDest;
	command = theCommand;
	value = theValue;
	setData(value);
}

/******************************************************************
*
******************************************************************/
message::~message()
{

}

/******************************************************************
*
******************************************************************/
int message::getDest()
{
	return dest;
}

/******************************************************************
*
******************************************************************/
int message::getSource()
{
	return source;
}

/******************************************************************
*
******************************************************************/
int message::getCommand()
{
	return command;
}

/******************************************************************
*
******************************************************************/
int message::getValue()
{
	return value;
}


/******************************************************************
*
******************************************************************/
void message::setData(string value)
{
	data_length = value.length();
	data = new char[data_length];
	for( int i = 0; i < value.length(); i++)
		data[i] = value[i];
}

/******************************************************************
*
******************************************************************/
char * message::getData()
{
	return data;
}

/******************************************************************
*
******************************************************************/
int message::getDataLength()
{
	return data_length;
}

/******************************************************************
*
******************************************************************/
string message::getString()
{
	return NULL;
}
